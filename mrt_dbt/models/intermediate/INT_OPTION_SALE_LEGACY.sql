/*
  데이터 업데이트가 과거에 끝난 건(2.0 PRODUCT, HOTEL)은 MART_SALE_D에서 가져옴.
  reservation_orders는 예약당 N개 옵션 존재(1:N).
  STRING_AGG로 예약당 1행으로 집계 후 JOIN하여 Row Explosion 방지.
*/
SELECT
    S.BASIS_DATE AS BASIS_DATE
  , S.RESVE_ID AS RESVE_ID
  , S.RESVE_ID AS RESVE_OPTION_ID
  , S.USER_ID AS USER_ID
  , S.ORDER_ID AS ORDER_ID
  , S.KIND AS KIND
  , S.PRODUCT_ID AS PRODUCT_ID
  , S.PRODUCT_ID AS OPTION_ID
  , S.GID AS GID
  , S.GID AS OPTION_GID
  , CASE
        WHEN S.DOMAIN_NM = 'HOTEL' THEN 'LEGACY-STAY'
        WHEN S.DOMAIN_NM = '2.0 PRODUCT' THEN 'LEGACY'
    END AS DOMAIN_NM
  , S.PRODUCT_TITLE AS PRODUCT_TITLE
  , O.TITLE AS OPTION_TITLE
  , S.CANCEL_KST_DT AS CANCEL_KST_DT
  , S.RECENT_STATUS AS RECENT_STATUS
  , S.TRAVEL_START_KST_DATE AS TRAVEL_START_KST_DATE
  , S.TRAVEL_END_KST_DATE AS TRAVEL_END_KST_DATE
  , S.PG_NM AS PG_NM
  , S.PLATFORM AS PLATFORM
  , S.RESVE_PAID_KST_DT AS RESVE_PAID_KST_DT
  , S.STANDARD_CATEGORY_LV_1_CD AS STANDARD_CATEGORY_LV_1_CD
  , S.STANDARD_CATEGORY_LV_1_NM AS STANDARD_CATEGORY_LV_1_NM
  , S.STANDARD_CATEGORY_LV_2_CD AS STANDARD_CATEGORY_LV_2_CD
  , S.STANDARD_CATEGORY_LV_2_NM AS STANDARD_CATEGORY_LV_2_NM
  , S.STANDARD_CATEGORY_LV_3_CD AS STANDARD_CATEGORY_LV_3_CD
  , S.STANDARD_CATEGORY_LV_3_NM AS STANDARD_CATEGORY_LV_3_NM
  , S.PARTNER_ID AS OPTION_PARTNER_ID
  , S.SALES_KRW_PRICE AS SALES_KRW_PRICE
  , S.PAID_KRW_PRICE AS PAID_PRICE
  , S.COUPON_PRICE AS COUPON_PRICE
  , S.COUPON_PRICE AS PRODUCT_COUPON_PRICE
  , 0 AS ORDER_COUPON_PRICE
  , CAST(NULL AS INT64) AS PRODUCT_COUPON_ID
  , CAST(NULL AS INT64) AS ORDER_COUPON_ID
  , S.POINT_PRICE AS POINT_PRICE
  , CAST(NULL AS STRING) AS PAYMENT_METHOD_VALUE
  , S.COMMISSION_RATE AS PAYMENT_COMMISSION_RATE
  , CAST(ROUND(S.SALES_PRICE * S.COMMISSION_RATE, 0) AS INT64) AS PAYMENT_COMMISSION_PRICE
  , S.COMMISSION_RATE AS SETTLEMENT_COMMISSION_RATE
  , CAST(ROUND(S.SALES_PRICE * S.COMMISSION_RATE, 0) AS INT64) AS SETTLEMENT_COMMISSION_PRICE
  , S.CITY_NM AS CITY_NM
  , S.COUNTRY_NM AS COUNTRY_NM
  , S.REGION_NM AS REGION_NM
  , S.RESVE_PURPOSE_TYPE AS RESVE_PURPOSE_TYPE
  , S.CREATE_KST_DT AS CREATE_KST_DT
  , S.UPDATE_KST_DT AS UPDATE_KST_DT
FROM {{ ref('MART_SALE_D') }} S
LEFT JOIN (
    SELECT
        reservation_id
      , STRING_AGG(title, ', ' ORDER BY id) AS TITLE
    FROM {{ source('mrt_20', 'reservation_orders') }}
    WHERE deleted_at IS NULL
    GROUP BY reservation_id
) O
  ON S.RESVE_ID = CAST(O.reservation_id AS STRING)
 AND S.DOMAIN_NM = '2.0 PRODUCT'
WHERE S.DOMAIN_NM IN ('HOTEL', '2.0 PRODUCT')
