  , CAST(NULL AS INT64) AS PRODUCT_COUPON_ID
  , CAST(NULL AS INT64) AS ORDER_COUPON_ID
  , R.POINT_PRICE AS POINT_PRICE
  , CAST(NULL AS STRING) AS PAYMENT_METHOD_VALUE
  , R.COMMISSION_RATE AS PAYMENT_COMMISSION_RATE
  , R.COMMISSION_PRICE AS PAYMENT_COMMISSION_PRICE
  , R.COMMISSION_RATE AS SETTLEMENT_COMMISSION_RATE
  , R.COMMISSION_PRICE AS SETTLEMENT_COMMISSION_PRICE
  , CT.CITY AS CITY_NM
  , CT.COUNTRY AS COUNTRY_NM
  , CT.REGION AS REGION_NM
  , CAST(NULL AS STRING) AS RESVE_PURPOSE_TYPE
  , R.CREATE_KST_DT AS CREATE_KST_DT
  , R.UPDATE_KST_DT AS UPDATE_KST_DT
FROM {{ ref('MART_OVERSEAS_RENTALCAR_SALE_D') }} R
LEFT JOIN {{ source('ups', 'union_product_v3') }} UPS
  ON UPS.id = 1000009
LEFT JOIN {{ source('mrt_mart_view', 'dim_standard_category') }} C
  ON UPS.standard_category_code = C.LV_3_CD
LEFT JOIN {{ ref("DIM_CITY") }} CT
  ON R.CITY_CD = CT.CODE
