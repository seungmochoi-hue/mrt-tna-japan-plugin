SELECT
    R.BASIS_DATE AS BASIS_DATE
  , R.RESVE_ID AS RESVE_ID
  , R.RESVE_ID AS RESVE_OPTION_ID
  , R.USER_ID AS USER_ID
  , R.ORDER_ID AS ORDER_ID
  , R.KIND AS KIND
  , '100000004' AS PRODUCT_ID
  , '100000004' AS OPTION_ID
  , '1000009' AS GID
  , '1000009' AS OPTION_GID
  , 'OVERSEAS RENTALCAR' AS DOMAIN_NM
  , CAST(NULL AS STRING) AS PRODUCT_TITLE
  , CAST(NULL AS STRING) AS OPTION_TITLE
  , R.CANCEL_KST_DT AS CANCEL_KST_DT
  , R.RECENT_STATUS AS RECENT_STATUS
  , R.TRAVEL_START_KST_DATE AS TRAVEL_START_KST_DATE
  , R.TRAVEL_END_KST_DATE AS TRAVEL_END_KST_DATE
  , R.PG_NM AS PG_NM
  , R.PLATFORM AS PLATFORM
  , R.RESVE_PAID_KST_DT AS RESVE_PAID_KST_DT
  , C.LV_1_CD AS STANDARD_CATEGORY_LV_1_CD
  , C.LV_1_NM AS STANDARD_CATEGORY_LV_1_NM
  , C.LV_2_CD AS STANDARD_CATEGORY_LV_2_CD
  , C.LV_2_NM AS STANDARD_CATEGORY_LV_2_NM
  , C.LV_3_CD AS STANDARD_CATEGORY_LV_3_CD
