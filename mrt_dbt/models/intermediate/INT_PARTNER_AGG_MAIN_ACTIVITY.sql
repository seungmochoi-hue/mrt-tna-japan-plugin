WITH PARTNER_MAIN_ACTIVITY AS (
    SELECT
        S.PARTNER_ID AS PARTNER_ID
      , 'CITY' AS DOMAIN_NM
      , S.CITY_CD AS PRODUCT_TYPE
      , COUNT(DISTINCT S.GID) AS PRODUCT_CNT
    FROM {{ ref('MART_SALE_D') }} S
    WHERE S.KIND = 1
      AND S.MRT_TYPE NOT IN ('flight', 'hotel')
      AND S.PARTNER_ID IS NOT NULL
    GROUP BY S.PARTNER_ID, S.CITY_CD

    UNION ALL

    SELECT
        S.PARTNER_ID AS PARTNER_ID
      , 'COUNTRY' AS DOMAIN_NM
      , S.COUNTRY_NM AS PRODUCT_TYPE
      , COUNT(DISTINCT S.GID) AS PRODUCT_CNT
    FROM {{ ref('MART_SALE_D') }} S
    WHERE S.KIND = 1
      AND S.MRT_TYPE NOT IN ('flight', 'hotel')
      AND S.PARTNER_ID IS NOT NULL
    GROUP BY S.PARTNER_ID, S.COUNTRY_NM

    UNION ALL

    SELECT
        S.PARTNER_ID AS PARTNER_ID
      , 'MRT_TYPE' AS DOMAIN_NM
      , S.MRT_TYPE AS PRODUCT_TYPE
      , COUNT(DISTINCT S.GID) AS PRODUCT_CNT
    FROM {{ ref('MART_SALE_D') }} S
    WHERE S.KIND = 1
      AND S.MRT_TYPE NOT IN ('flight', 'hotel')
      AND S.PARTNER_ID IS NOT NULL
    GROUP BY S.PARTNER_ID, S.MRT_TYPE
)
SELECT
    CAST(T.PARTNER_ID AS STRING) AS PARTNER_ID
  , CAST(NULL AS INT64) AS RESVE_CNT
  , CAST(NULL AS INT64) AS SALES_KRW_PRICE
  , CAST(NULL AS INT64) AS USER_CNT
  , CAST(NULL AS INT64) AS CURRENT_RESVE_CNT
  , CAST(NULL AS INT64) AS ONSALE_CNT
  , MAX(IF(T.DOMAIN_NM = 'CITY', T.PRODUCT_TYPE, NULL)) AS MAIN_ACTIVITY_CITY_CD
  , MAX(IF(T.DOMAIN_NM = 'COUNTRY', T.PRODUCT_TYPE, NULL)) AS MAIN_ACTIVITY_COUNTRY_NM
  , MAX(IF(T.DOMAIN_NM = 'MRT_TYPE', T.PRODUCT_TYPE, NULL)) AS MAIN_ACTIVITY_MRT_TYPE
  , CAST(NULL AS INT64) AS REVIEW_CNT
  , CAST(NULL AS FLOAT64) AS REVIEW_SCORE_AVG
  , CAST(NULL AS FLOAT64) AS CONFIRM_RATE
FROM (
    SELECT
        P.PARTNER_ID
      , P.DOMAIN_NM
      , P.PRODUCT_TYPE
      , DENSE_RANK() OVER (
            PARTITION BY P.PARTNER_ID, P.DOMAIN_NM
            ORDER BY P.PRODUCT_CNT DESC
        ) AS RANK
    FROM PARTNER_MAIN_ACTIVITY P
) T
WHERE T.RANK = 1
GROUP BY CAST(T.PARTNER_ID AS STRING)
