SELECT
    CAST(S.PARTNER_ID AS STRING) AS PARTNER_ID
  , COUNT(DISTINCT IF(S.RECENT_STATUS <> 'cancel', S.RESVE_ID, NULL)) AS RESVE_CNT
  , CAST(SUM(IF(S.RECENT_STATUS <> 'cancel', S.PAID_KRW_PRICE, 0)) AS INT64) AS SALES_KRW_PRICE
  , COUNT(DISTINCT IF(S.RECENT_STATUS <> 'cancel', S.USER_ID, NULL)) AS USER_CNT
  , COUNT(DISTINCT IF(S.TRAVEL_START_KST_DATE >= '{{ var("logical_start_date_kst") }}' AND S.RECENT_STATUS <> 'cancel', S.RESVE_ID, NULL)) AS CURRENT_RESVE_CNT
  , CAST(NULL AS INT64) AS ONSALE_CNT
  , CAST(NULL AS STRING) AS MAIN_ACTIVITY_CITY_CD
  , CAST(NULL AS STRING) AS MAIN_ACTIVITY_COUNTRY_NM
  , CAST(NULL AS STRING) AS MAIN_ACTIVITY_MRT_TYPE
  , CAST(NULL AS INT64) AS REVIEW_CNT
  , CAST(NULL AS FLOAT64) AS REVIEW_SCORE_AVG
  , CAST(NULL AS FLOAT64) AS CONFIRM_RATE
FROM {{ ref('MART_SALE_D') }} S
WHERE S.KIND = 1
  AND S.PARTNER_ID IS NOT NULL
GROUP BY CAST(S.PARTNER_ID AS STRING)
