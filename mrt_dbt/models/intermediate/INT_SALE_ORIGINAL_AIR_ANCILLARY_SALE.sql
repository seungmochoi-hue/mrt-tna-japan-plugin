SELECT
    A.BASIS_DATE AS BASIS_DATE
  , A.RESVE_UNIQUE_ID AS RESVE_ID
  , A.RESVE_UNIQUE_ID AS ORDER_ID
  , CAST(NULL AS STRING) AS ORDER_NO
  , MIN_BY(A.USER_ID, LENGTH(A.USER_ID)) AS USER_ID
  , A.KIND AS KIND
  , MAX_BY(A.RECENT_STATUS, CASE A.RECENT_STATUS WHEN 'reserved' THEN 1 WHEN 'paid' THEN 2 WHEN 'cancelled' THEN 3 END) AS RECENT_STATUS
  , MIN(CAST(A.CANCEL_KST_DATE AS TIMESTAMP)) AS CANCEL_KST_DT
  , 'AIR ANCILLARY' AS DOMAIN_NM
  , '100000010' AS PRODUCT_ID
  , '2000006' AS GID
  , CAST(NULL AS STRING) AS GPID
  , MIN(A.CREATED_KST_DT) AS CONFIRM_KST_DT
  , MIN(CAST(A.DEPART_KST_DT AS DATE)) AS TRAVEL_START_KST_DATE
  , MAX(CAST(A.ARRIVE_KST_DT AS DATE)) AS TRAVEL_END_KST_DATE
  , 'flight ancillary' AS RESVE_TYPE
  , CAST(NULL AS STRING) AS ORDER_RESVE_TYPE
  , A.SALE_FORM_CD AS SALE_FORM_CD
  , CAST(NULL AS STRING) AS SALE_FORM_TYPE
  , A.SALE_AGG_TYPE AS SALE_AGG_TYPE
  , CAST(NULL AS STRING) AS BRANCH_CD
  , CAST(NULL AS STRING) AS PARTNER_ID
  , 'AIR_ANCILLARY' AS STANDARD_CATEGORY_LV_1_CD
  , 'AIR_ANCILLARY' AS STANDARD_CATEGORY_LV_2_CD
  , 'AIR_ANCILLARY' AS STANDARD_CATEGORY_LV_3_CD
  , CAST(NULL AS STRING) AS PAYMENT_LATER_FLAG
  , CAST(NULL AS TIMESTAMP) AS PAYMENT_LIMIT_KST_DT
  , CAST(NULL AS STRING) AS PARTNERSHIP_CD
  , CAST(NULL AS STRING) AS PARTNERSHIP_TYPE
  , CAST(NULL AS STRING) AS MARKETING_PARTNERSHIP_CD
  , CAST(NULL AS STRING) AS MARKETING_LINK_ID
  , CAST(NULL AS STRING) AS PARTNERSHIP_PARTNER_ID
  , MIN_BY(A.STOCK_AIRLINE_CD, A.DEPART_KST_DT) AS PROVIDER_CD
  , MAX_BY(A.ARRIVE_AIRPORT_CD, A.ARRIVE_KST_DT) AS CITY_CD
  , CAST(NULL AS STRING) AS CITY_REPRESENTATIVE_FLAG
  , CAST(NULL AS STRING) AS PG_NM
  , MIN(A.CREATED_KST_DT) AS RESVE_PAID_KST_DT
  , CAST(NULL AS FLOAT64) AS SALES_PRICE
  , CAST(NULL AS STRING) AS SALES_PRICE_CUR_TYPE
  , SUM(A.SALES_KRW_PRICE) AS SALES_KRW_PRICE
  , CAST(NULL AS FLOAT64) AS PAID_PRICE
  , CAST(NULL AS STRING) AS PAID_PRICE_CUR_TYPE
  , SUM(A.SALES_KRW_PRICE) AS PAID_KRW_PRICE
  , 0 AS COUPON_PRICE
  , 0 AS PRODUCT_COUPON_PRICE
  , 0 AS ORDER_COUPON_PRICE
  , CAST(NULL AS INT64) AS PRODUCT_COUPON_ID
  , CAST(NULL AS INT64) AS ORDER_COUPON_ID
  , 0 AS POINT_PRICE
  , 0 AS DISCOUNT_PRICE
  , CAST(NULL AS FLOAT64) AS COMMISSION_PRICE
  , CAST(NULL AS FLOAT64) AS COMMISSION_RATE
  , CAST(NULL AS FLOAT64) AS SETTLEMENT_COMMISSION_PRICE
  , CAST(NULL AS FLOAT64) AS SETTLEMENT_COMMISSION_RATE
  , CAST(NULL AS STRING) AS RESVE_PURPOSE_TYPE
  , CAST(NULL AS INT64) AS CHILD_RESVE_PRSNL_CNT
  , CAST(SUM(A.RESVE_CNT) AS INT64) AS ADULT_RESVE_PRSNL_CNT
  , CAST(SUM(A.RESVE_CNT) AS INT64) AS RESVE_PRSNL_CNT
  , CAST(NULL AS STRING) AS PLATFORM
  , MIN(A.CREATED_KST_DT) AS CREATE_KST_DT
  , MAX(A.UPDATED_KST_DT) AS UPDATE_KST_DT
  , CAST(NULL AS INT64) AS PACKAGE_PNR_NO
FROM {{ ref('MART_AIR_ANCILLARY_SALE_D') }} A
GROUP BY A.BASIS_DATE, A.RESVE_UNIQUE_ID, A.KIND, A.SALE_FORM_CD, A.SALE_AGG_TYPE
