WITH AIR_SALE_PRICE AS (
    SELECT
        A.BASIS_DATE AS BASIS_DATE
      , A.PNR_NO AS PNR_NO
      , A.KIND AS KIND
      , SUM(A.SALE_TOTAL_PRICE) AS PAID_PRICE
      , SUM(A.DISCOUNT_BEFORE_NET_PRICE) - SUM(A.DISCOUNT_AFTER_NET_PRICE) AS DISCOUNT_PRICE
      , SUM(A.CHILD_RESVE_TICKET_CNT) AS CHILD_RESVE_PRSNL_CNT
      , SUM(A.ADULT_RESVE_TICKET_CNT) AS ADULT_RESVE_PRSNL_CNT
    FROM {{ ref('MART_AIR_SALE_D') }} A
    WHERE A.REISUE_FLAG <> 'R'
      AND A.TK110_MATCH_FLAG = 'Y'
    GROUP BY A.BASIS_DATE, A.PNR_NO, A.KIND
)
SELECT DISTINCT
    A.BASIS_DATE AS BASIS_DATE
  , CONCAT('f', A.PNR_NO) AS RESVE_ID
  , CONCAT('f', A.PNR_NO) AS ORDER_ID
  , CAST(NULL AS STRING) AS ORDER_NO
  , A.USER_ID AS USER_ID
  , A.KIND AS KIND
  , A.RECENT_STATUS AS RECENT_STATUS
  , A.CANCEL_KST_DT AS CANCEL_KST_DT
  , 'AIR' AS DOMAIN_NM
  , '100000001' AS PRODUCT_ID
  , A.GID AS GID -- 시리즈 발권의 경우 GID를, 그 외엔 '1000006'을 넣어준다
  , CAST(NULL AS STRING) AS GPID
  , CAST(A.ISSUE_KST_DATE AS TIMESTAMP) AS CONFIRM_KST_DT
  , CASE
        WHEN A.DOMESTIC_INTERNATIONAL_DIV_CD = 'D'
         AND A.PNR_NO > safe_cast(A.SUPER_PNR_NO AS int64)
            THEN CAST(S.dep_dtm AS DATE)
        ELSE CAST(A.DEPART_KST_DT AS DATE)
    END AS TRAVEL_START_KST_DATE -- 국내선 왕편의 출발일을 왕복편의 출발일로 지정
  , CASE
        WHEN A.DOMESTIC_INTERNATIONAL_DIV_CD = 'D'
         AND A.PNR_NO < safe_cast(A.SUPER_PNR_NO AS int64)
            THEN CAST(S.dep_dtm AS DATE)
        WHEN A.DOMESTIC_INTERNATIONAL_DIV_CD = 'D'
         AND A.PNR_NO > safe_cast(A.SUPER_PNR_NO AS int64)
            THEN CAST(A.DEPART_KST_DT AS DATE)
        ELSE CAST(A.RETURN_KST_DT AS DATE)
    END AS TRAVEL_END_KST_DATE -- 국내선 복편의 출발일을 왕복편 모두 귀국일로 지정
  , 'flight' AS RESVE_TYPE
  , CAST(NULL AS STRING) AS ORDER_RESVE_TYPE
  , A.SALE_FORM_CD AS SALE_FORM_CD
  , A.SALE_FORM_TYPE AS SALE_FORM_TYPE
  , A.SALE_AGG_TYPE AS SALE_AGG_TYPE
  , A.BRANCH_CD AS BRANCH_CD
  , A.BPLC_CD AS PARTNER_ID
  , C.LV_1_CD AS STANDARD_CATEGORY_LV_1_CD
  , C.LV_2_CD AS STANDARD_CATEGORY_LV_2_CD
  , C.LV_3_CD AS STANDARD_CATEGORY_LV_3_CD
  , CAST(NULL AS STRING) AS PAYMENT_LATER_FLAG
  , CAST(NULL AS TIMESTAMP) AS PAYMENT_LIMIT_KST_DT
  , CAST(NULL AS STRING) AS PARTNERSHIP_CD
  , CAST(NULL AS STRING) AS PARTNERSHIP_TYPE
  , A.MARKETING_PARTNERSHIP_CD AS MARKETING_PARTNERSHIP_CD
  , A.MARKETING_LINK_ID AS MARKETING_LINK_ID
  , CAST(NULL AS STRING) AS PARTNERSHIP_PARTNER_ID
  , A.AIRLINE_CD AS PROVIDER_CD
  , A.ARRIVE_AIRPORT_CD AS CITY_CD
  , CAST(NULL AS STRING) AS CITY_REPRESENTATIVE_FLAG
  , CAST(NULL AS STRING) AS PG_NM
  , A.RESVE_KST_DT AS RESVE_PAID_KST_DT
  , P.PAID_PRICE + P.DISCOUNT_PRICE AS SALES_PRICE
  , 'KRW' AS SALES_PRICE_CUR_TYPE
  , P.PAID_PRICE + P.DISCOUNT_PRICE AS SALES_KRW_PRICE
  , P.PAID_PRICE AS PAID_PRICE
  , 'KRW' AS PAID_PRICE_CUR_TYPE
  , P.PAID_PRICE AS PAID_KRW_PRICE
  , 0 AS COUPON_PRICE
  , 0 AS PRODUCT_COUPON_PRICE
  , 0 AS ORDER_COUPON_PRICE
  , CAST(NULL AS INT64) AS PRODUCT_COUPON_ID
  , CAST(NULL AS INT64) AS ORDER_COUPON_ID
  , CAST(A.POINT_PRICE AS INT) AS POINT_PRICE
  , P.DISCOUNT_PRICE AS DISCOUNT_PRICE
  , CAST(NULL AS FLOAT64) AS COMMISSION_PRICE
  , CAST(NULL AS FLOAT64) AS COMMISSION_RATE
  , CAST(NULL AS FLOAT64) AS SETTLEMENT_COMMISSION_PRICE
  , CAST(NULL AS FLOAT64) AS SETTLEMENT_COMMISSION_RATE
  , CAST(NULL AS STRING) AS RESVE_PURPOSE_TYPE
  , P.CHILD_RESVE_PRSNL_CNT AS CHILD_RESVE_PRSNL_CNT
  , P.ADULT_RESVE_PRSNL_CNT AS ADULT_RESVE_PRSNL_CNT
  , P.CHILD_RESVE_PRSNL_CNT + P.ADULT_RESVE_PRSNL_CNT AS RESVE_PRSNL_CNT
  , A.PAYMENT_PLATFORM AS PLATFORM
  , A.RESVE_KST_DT AS CREATE_KST_DT
  , A.RESVE_KST_DT AS UPDATE_KST_DT
  , CAST(NULL AS INT64) AS PACKAGE_PNR_NO
FROM {{ ref('MART_AIR_SALE_D') }} A
LEFT JOIN AIR_SALE_PRICE P
  ON A.BASIS_DATE = P.BASIS_DATE
 AND A.PNR_NO = P.PNR_NO
 AND A.KIND = P.KIND
LEFT JOIN {{ source('air', 'TB_AIR_RV100') }} S
  ON A.PNR_NO = safe_cast(S.super_rsv_no AS int64)
LEFT JOIN {{ source('ups', 'union_product_v3') }} UPS
  ON UPS.id = 1000006
LEFT JOIN {{ source('mrt_mart_view', 'dim_standard_category') }} C
  ON UPS.standard_category_code = C.LV_3_CD
LEFT JOIN (
    SELECT DISTINCT
        A.PNR_NO
    FROM {{ ref('MART_AIR_SALE_D') }} A
    WHERE A.SALE_FORM_CD = 'B2C'
      AND A.REISUE_FLAG = 'N'
      AND A.ON_OFF_RESVE_FLAG = 'OFF'
) K
  ON A.PNR_NO = K.PNR_NO
WHERE A.REISUE_FLAG <> 'R'
  AND K.PNR_NO IS NULL
  AND A.SALE_FORM_CD NOT IN ('B2BCON', 'PKG')
  AND A.SALE_AGG_TYPE <> 'PACKAGE'
  AND A.PACKAGE_RESVE_ID IS NULL
