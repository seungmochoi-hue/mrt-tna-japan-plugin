  , B.CONFIRM_KST_DT AS CONFIRM_KST_DT
  , CASE
        WHEN B.RECENT_STATUS = 'cancel'
         AND B.CONFIRM_KST_DATE IS NULL
         AND C.CLOSING_BASIS_DATE IS NOT NULL
        THEN C.CLOSING_BASIS_DATE
        ELSE B.CONFIRM_KST_DATE
    END AS CONFIRM_KST_DATE
  -- 취소 접수일: 예약 시스템(reservations)에서 취소/환불 이벤트가 발생한 날짜
  -- 원천: MART_SALE_D.CANCEL_KST_DT (숙박: hotel_reservupdate_detail.reserv_date_kst, 서비스: reservation_status_logs.created_at_kst)
  , B.CANCEL_DATE AS CANCEL_DATE
  , B.CREATE_KST_DATE AS CREATE_KST_DATE
