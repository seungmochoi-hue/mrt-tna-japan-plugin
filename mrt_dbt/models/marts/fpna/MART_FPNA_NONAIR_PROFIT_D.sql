/*
  [MART_FPNA_NONAIR_PROFIT_D]

  FPNA 비항공 수익/비용/지표 산출용 일 단위 MART.
  도메인 mart(숙박/렌터카/T&A/패키지/보험) + reservations + closing + mapping을 직접 받아 계산한다.
  Grain은 주문/예약 단위(1 row = 1 예약).

  ## 주요 컬럼 산출 로직

  ### 1. REVENUE (Closing 기준 Revenue, VAT 제외)
  - Closing(SALE_COMMISSION + MRT_CANCEL_COMMISSION) / 1.1을 우선 사용
  - Closing 미매칭 건: 기존 로직 폴백 (사입: GMV/1.1, 위탁: MRT_SALES_PRICE)
  - 매관 비교 시 view에서 REVENUE * 1.1로 VAT 포함 금액 산출

  ### 2. MRT_POINT_NET_AMOUNT (포인트 순액)
  - POINT_PRICE - EXCLUDED_POINT_PRICE
*/

