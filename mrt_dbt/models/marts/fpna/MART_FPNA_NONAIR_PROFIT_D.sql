  , B.EXCLUDED_POINT_PRICE AS EXCLUDED_POINT_PRICE
  , B.DISCOUNT_PRICE AS DISCOUNT_PRICE
  , B.MARKETING_PARTNER_FEE AS MARKETING_PARTNER_FEE
  , B.AGENCY_FEE AS AGENCY_FEE
  , B.AFFILIATE_POINT_FEE AS AFFILIATE_POINT_FEE
  , B.CHANNEL_FEE_PRICE AS CHANNEL_FEE_PRICE
