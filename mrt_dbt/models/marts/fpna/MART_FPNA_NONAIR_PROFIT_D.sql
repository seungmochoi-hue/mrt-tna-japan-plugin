{% endfor %}
, NONAIR_UNION AS (
    {% for d in domain_configs %}
    SELECT
        T.*
      , DATETIME_ADD(CURRENT_TIMESTAMP(), INTERVAL 9 HOUR) AS DW_LOAD_DT
    FROM {{ d['cte'] }} T
    {% if not loop.last %}UNION ALL{% endif %}
    {% endfor %}
)
, NONAIR_BASE AS (
    /*
      [CONFIRM_KST_DATE 보정]
      - 확정 전 취소(주로 WAIT_CONFIRM 단계에서 취소 완료) 케이스는 `orders.reservations.confirmed_at`이 NULL로 남아 CONFIRM_KST_DATE/CONFIRM_MONTH가 NULL인 경우가 존재한다.
      - `VIEW_FPNA_NONAIR_CGMV_REVENUE_EVENT_M(_V2)` 이벤트형 집계는 `CONFIRM_MONTH IS NOT NULL` 필터를 사용하므로, 이 케이스는 CONFIRM/CANCEL 이벤트가 모두 누락되어 FPNA Confirm CGMV가 과소 집계되고 Closing과 차이가 발생한다.
      - RECENT_STATUS='cancel' AND CONFIRM_KST_DATE IS NULL 인 건에 한해, Closing의 BASIS_DATE(=confirmation_date 기반, 대사용 기준일)로 보정한다.
      - Closing은 옵션/상품 단위이므로 (ORDER_NO, RESERVATION_NO)로 사전 집계하여 row explosion을 방지한다.
      - wait_confirm은 제외한다. (추후 확정될 수 있음)
    */
    SELECT
        S.*
      , COALESCE(R_NO.reservation_no, R_ID.reservation_no) AS RESERVATION_NO_FOR_CLOSING
    FROM NONAIR_UNION S
    LEFT JOIN {{ source('orders', 'reservations') }} R_NO
      ON S.RESVE_ID = R_NO.reservation_no
     AND R_NO.deleted_at IS NULL
    LEFT JOIN {{ source('orders', 'reservations') }} R_ID
      ON R_NO.reservation_no IS NULL
     AND S.RESVE_ID = CAST(R_ID.id AS STRING)
     AND R_ID.deleted_at IS NULL
)
, CLOSING_BASIS_DATE AS (
    SELECT
        C.ORDER_NO AS ORDER_NO
      , C.RESERVATION_NO AS RESERVATION_NO
      , MIN(C.BASIS_DATE) AS CLOSING_BASIS_DATE
    FROM {{ ref('MART_FPNA_CONFIRMATION_CLOSING_D') }} C
    GROUP BY
        C.ORDER_NO
      , C.RESERVATION_NO
)
-- 결제 건 Revenue/PG_AMOUNT를 (ORDER_NO, RESERVATION_NO) 단위로 사전 집계
, CLOSING_PAYMENT_METRICS AS (
    SELECT
        C.ORDER_NO AS ORDER_NO
      , C.RESERVATION_NO AS RESERVATION_NO
      , SUM(C.SALE_COMMISSION + C.MRT_CANCEL_COMMISSION) AS PAYMENT_REVENUE
      , SUM(C.PG_AMOUNT) AS PAYMENT_PG_AMOUNT
    FROM {{ ref('MART_FPNA_CONFIRMATION_CLOSING_D') }} C
    WHERE C.ORDER_TYPE = '결제'
    GROUP BY
        C.ORDER_NO
      , C.RESERVATION_NO
)
, REGION_TNA_MAPPING AS (
    SELECT
        CITY_NM_KEY
      , COUNTRY_NM_KEY
      , REGION_NM_KEY
      , REGION_TNA_NM
    FROM {{ ref('INT_REGION_TNA_MAPPING_KEY') }}
)
SELECT
