  , B.GMV AS GMV
  , B.MRT_SALES_PRICE AS MRT_SALES_PRICE
  , B.PRODUCT_COUPON_PRICE AS PRODUCT_COUPON_PRICE
  , B.ORDER_COUPON_PRICE AS ORDER_COUPON_PRICE
  , B.COUPON_PRICE AS COUPON_PRICE
