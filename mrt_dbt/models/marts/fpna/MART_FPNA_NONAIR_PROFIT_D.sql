WITH
{% for d in domain_configs %}
{{ d['cte'] }} AS (
    -- {{ d['comment'] }}
    {{ fpna_nonair_profit_domain_select(ref(d['source_model']), d['src_alias'], d['fpna_domain_nm']) }}
