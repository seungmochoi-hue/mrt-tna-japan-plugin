  , B.CHANNEL_FEE_AMOUNT AS CHANNEL_FEE_AMOUNT
  , B.CON_MARGIN AS CON_MARGIN
  , B.COMMISSION_RATE AS COMMISSION_RATE
  , B.NET_PRICE AS NET_PRICE
  , B.DW_LOAD_DT AS DW_LOAD_DT
  , M.REGION_TNA_NM AS REGION_TNA_NM
  -- Revenue (VAT 제외): Closing 결제 Revenue 우선, 미매칭 시 기존 로직 폴백
  , COALESCE(
        CP.PAYMENT_REVENUE / 1.1
      , CASE
            WHEN B.PARTNER_SALES_TYPE = 'total' THEN B.GMV / 1.1
            ELSE B.MRT_SALES_PRICE
        END
    ) AS REVENUE
  -- 포인트 순액
  , B.POINT_PRICE - B.EXCLUDED_POINT_PRICE AS MRT_POINT_NET_AMOUNT
FROM NONAIR_BASE B
LEFT JOIN CLOSING_BASIS_DATE C
  ON B.ORDER_NO = C.ORDER_NO
 AND B.RESERVATION_NO_FOR_CLOSING = C.RESERVATION_NO
LEFT JOIN CLOSING_PAYMENT_METRICS CP
  ON B.ORDER_NO = CP.ORDER_NO
 AND B.RESERVATION_NO_FOR_CLOSING = CP.RESERVATION_NO
LEFT JOIN REGION_TNA_MAPPING M
  ON B.CITY_NM = M.CITY_NM_KEY
 AND B.COUNTRY_NM = M.COUNTRY_NM_KEY
 AND B.REGION_NM = M.REGION_NM_KEY
