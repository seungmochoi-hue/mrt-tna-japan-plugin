  , B.STANDARD_CATEGORY_LV_2_CD AS STANDARD_CATEGORY_LV_2_CD
  , B.STANDARD_CATEGORY_LV_2_NM AS STANDARD_CATEGORY_LV_2_NM
  , B.STANDARD_CATEGORY_LV_3_CD AS STANDARD_CATEGORY_LV_3_CD
  , B.STANDARD_CATEGORY_LV_3_NM AS STANDARD_CATEGORY_LV_3_NM
  , B.BIZ_TYPE AS BIZ_TYPE
  , B.BIZ_TYPE_V2 AS BIZ_TYPE_V2
  , B.BIZ_TYPE_V3 AS BIZ_TYPE_V3
  , B.FPNA_TYPE_LV_1 AS FPNA_TYPE_LV_1
  , B.FPNA_TYPE_LV_2 AS FPNA_TYPE_LV_2
  , B.FPNA_TYPE_LV_3 AS FPNA_TYPE_LV_3
  , B.MRT_TYPE AS MRT_TYPE
  , B.CATEGORY_CD AS CATEGORY_CD
  , B.CATEGORY_NM AS CATEGORY_NM
  , B.SUB_CATEGORY_CD AS SUB_CATEGORY_CD
  , B.FPNA_CATEGORY AS FPNA_CATEGORY
  , B.TEAM_DIVISION AS TEAM_DIVISION
  , B.GID AS GID
  , B.GPID AS GPID
  , B.PRODUCT_ID AS PRODUCT_ID
  , B.PRODUCT_TITLE AS PRODUCT_TITLE
  , B.REGION_TYPE AS REGION_TYPE
  , B.REGION_NM AS REGION_NM
  , B.COUNTRY_NM AS COUNTRY_NM
  , B.CITY_NM AS CITY_NM
  , B.JEJU_FLAG AS JEJU_FLAG
  , B.DOMAIN_NM AS DOMAIN_NM
  , B.FPNA_DOMAIN_NM AS FPNA_DOMAIN_NM
  , B.SALE_FORM_CD AS SALE_FORM_CD
  , B.B2B_COUPON_FLAG AS B2B_COUPON_FLAG
  , B.B2B_POINT_FLAG AS B2B_POINT_FLAG
  , B.B2B_AGENCY_FLAG AS B2B_AGENCY_FLAG
  , B.MARKETING_PARTNER_FLAG AS MARKETING_PARTNER_FLAG
  , B.ORDER_ID AS ORDER_ID
  , B.ORDER_NO AS ORDER_NO
  , B.RESVE_ID AS RESVE_ID
  , B.RECENT_STATUS AS RECENT_STATUS
  , B.RESVE_PRSNL_CNT AS RESVE_PRSNL_CNT
  , B.CROSS_SELL_FLAG AS CROSS_SELL_FLAG
  , B.COUPON_ID AS COUPON_ID
  , B.PRODUCT_COUPON_ID AS PRODUCT_COUPON_ID
  , B.ORDER_COUPON_ID AS ORDER_COUPON_ID
  , B.COUPON_TITLE AS COUPON_TITLE
  , B.PG_NM AS PG_NM
  , B.FLIGHT_RESVE_ID AS FLIGHT_RESVE_ID
  , B.FLIGHT_CREATE_KST_DT AS FLIGHT_CREATE_KST_DT
