        , STRING_AGG(CASE pax_sex WHEN '1' THEN 'M' WHEN '2' THEN 'F' END LIMIT 1) AS rep_sex
        , STRING_AGG(DISTINCT CONCAT(
            pax_birth_year
            , '-'
            , CASE pax_sex WHEN '1' THEN 'M' WHEN '2' THEN 'F' END
        ) ORDER BY CONCAT(
            pax_birth_year
            , '-'
            , CASE pax_sex WHEN '1' THEN 'M' WHEN '2' THEN 'F' END)
        ) AS ages
        FROM {{ source('air', 'DIM_PREDICTIVE_USR_TB_AIR_RV110') }}
        GROUP BY reg_usr_id
),
COUPON AS (
    SELECT
        CAST(user_id AS STRING) AS user_id
        , COUNT(DISTINCT id) AS total_coupon_cnt
        , COUNT(DISTINCT CASE WHEN used_at IS NULL THEN id END) AS not_used_coupon_cnt
        , COUNT(DISTINCT CASE WHEN used_at IS NOT NULL THEN id END) AS used_coupon_cnt
        , COUNT(DISTINCT CASE WHEN used_at IS NULL AND expire_date >= '{{ var("logical_start_date_kst") }} 00:00:00' THEN id END) AS remain_coupon_cnt
        , STRING_AGG(DISTINCT CASE WHEN used_at IS NULL AND expire_date >= '{{ var("logical_start_date_kst") }} 00:00:00' THEN CAST(template_id AS STRING) END, ",") AS remain_coupon_ids
    FROM {{ source('coupon', 'coupon_user_mapping') }}
    WHERE deleted_at IS NULL
        AND use_status IN ('USE', 'PUBLISHED')
    GROUP BY user_id
)
, REVIEWS AS (
    SELECT
        user_id
        , SUM(review_cnt) AS review_cnt
    FROM (
        SELECT
            CAST(user_id AS STRING) AS user_id
            , COUNT(DISTINCT reservation_id) AS review_cnt
        FROM {{ source('mrt_20', 'reviews') }}
        WHERE deleted_at IS NULL
        GROUP BY user_id
        UNION ALL
        SELECT
            CAST(r.user_id AS STRING) AS user_id
            , COUNT(DISTINCT r.id) AS review_cnt
        FROM {{ source('reviews', 'reviews') }} AS r
        JOIN {{ source('reviews', 'review_reservation_mappings') }} AS m ON CAST(r.id AS INT) = m.review_id
        WHERE m.reservation_no IS NOT NULL AND r.deleted_at IS NULL
        GROUP BY user_id
        UNION ALL
        SELECT
            CAST(user_id AS STRING) AS user_id
            , COUNT(DISTINCT id) AS review_cnt
        FROM edw.DW_MRT_PRODUCTS_PRODUCT_REVIEWS -- 실제 테이블은 없어짐 ( 24년 이 마지막)
        WHERE deleted_at IS NULL
        GROUP BY user_id
    )
    GROUP BY user_id
)
