                WHEN MRT_TYPE IN ('flight') THEN 'FLIGHT'
                WHEN MRT_TYPE IN ('hotel', 'pension', 'lodging', 'ticket_lodging') THEN 'STAY'
                WHEN MRT_TYPE IN ('rentalcar', 'ticket_rentalcar') THEN 'RIDE'
                WHEN MRT_TYPE IN ('tour', 'ticket') THEN 'TNA'
                ELSE 'ETC'
            END AS MRT_GROUP_TYPE
            , CASE
                WHEN IFNULL(TRAVEL_END_KST_DATE, TRAVEL_START_KST_DATE) < '{{ var("logical_start_date_kst") }}' THEN 'RECENT'
                WHEN IFNULL(TRAVEL_END_KST_DATE, TRAVEL_START_KST_DATE) >= '{{ var("logical_start_date_kst") }}' THEN 'EXPT'
            END AS BASIS_TYPE
            , MAX(KIND) OVER(PARTITION BY USER_ID, RESVE_ID) AS RESVE_CNT
            FROM {{ ref('MART_SALE_D') }}
            WHERE user_id IS NOT NULL AND user_id NOT IN ('smart_guest', 'guest')
        ) WHERE RESVE_CNT=1 AND CONFIRM_KST_DT IS NOT NULL
    )
    GROUP BY USER_ID
